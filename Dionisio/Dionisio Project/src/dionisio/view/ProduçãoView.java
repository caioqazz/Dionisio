    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTProducao.getModel();
        modelo.setNumRows(0);
        ProduçãoDAO pdao = new ProduçãoDAO();
        ArrayList<Produção> lista = pdao.read();
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new Object[]{
                lista.get(i).getId_colheita(),
                lista.get(i).getId_safra()
            });
        }
    }

    public void setComboIds() {
        ProduçãoDAO pdao = new ProduçãoDAO();
        ArrayList<Integer> c = pdao.getIdColheita();
        for (int i = 0; i < c.size(); i++) {
            idcolheita.addItem(c.get(i) + "");

        }
        ArrayList<Integer> s = pdao.getIdSafras();
        for (int i = 0; i < s.size(); i++) {
            idsafra.addItem(s.get(i) + "");

        }
    }

