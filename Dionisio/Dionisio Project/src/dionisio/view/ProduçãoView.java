    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTProducao.getModel();
        ProduçãoDAO pdao = new ProduçãoDAO();
        if (jTProducao.getSelectedRow() >= 0) {
            Object id_safra = jTProducao.getValueAt(jTProducao.getSelectedRow(), 1);
            Object id_colheita = jTProducao.getValueAt(jTProducao.getSelectedRow(), 0);
            pdao.delete(Integer.parseInt(id_safra.toString()), Integer.parseInt(id_colheita.toString()));
            readJTable();
        } else {

            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

