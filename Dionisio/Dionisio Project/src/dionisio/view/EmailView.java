    private void AdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdicionarActionPerformed
        Email email = new Email((String) nome.getSelectedItem(), end.getText());
        EmailDAO emaildao = new EmailDAO();

        emaildao.create(email);
        readJTable();
        limpaCampos();
    }//GEN-LAST:event_AdicionarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTEmails.getModel();
        EmailDAO emaildao = new EmailDAO();
        if (jTEmails.getSelectedRow() >= 0) {
            Object end = jTEmails.getValueAt(jTEmails.getSelectedRow(), 1);
            emaildao.delete(end.toString());
            readJTable();
        } else {

            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

