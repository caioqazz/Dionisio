    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTEmails.getModel();
        modelo.setNumRows(0);
        EmailDAO edao = new EmailDAO();
        ArrayList<Email> lista = edao.read();
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new Object[]{
                lista.get(i).getNome(),
                lista.get(i).getEndereço()
            });
        }
    }

    public void setComboNomes() {
        EmailDAO edao = new EmailDAO();
        ArrayList<String> nomes = edao.getNomes();
        for (int i = 0; i < nomes.size(); i++) {
            nome.addItem(nomes.get(i));

        }
    }

