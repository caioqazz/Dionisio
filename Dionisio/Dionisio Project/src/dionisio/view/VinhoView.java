    private byte[] getImagem() throws IOException {
        boolean isPng = false;
        byte[] byteArray;
        if (imagem != null) {
            isPng = imagem.getName().endsWith("png");
            try {
                BufferedImage image = ImageIO.read(imagem);
                ByteArrayOutputStream out = new ByteArrayOutputStream();
                int type = BufferedImage.TYPE_INT_RGB;
                if (isPng) {
                    type = BufferedImage.BITMASK;
                }

                BufferedImage novaImagem = new BufferedImage(jLabel7.getWidth() - 5, jLabel7.getHeight() - 10, type);
                Graphics2D g = novaImagem.createGraphics();
                g.setComposite(AlphaComposite.Src);
                g.drawImage(image, 0, 0, jLabel7.getWidth() - 5, jLabel7.getHeight() - 10, this);
                if (isPng) {
                    ImageIO.write(image, "png", out);
                } else {
                    ImageIO.write(image, "jpg", out);
                }
                out.flush();
                byteArray = out.toByteArray();
                out.close();
                return byteArray;
            } catch (IOException e) {

            }
        }
        return null;
    }

