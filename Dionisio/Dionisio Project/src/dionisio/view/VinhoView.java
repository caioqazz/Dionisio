    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTVinhos.getModel();
        modelo.setNumRows(0);
        VinhoDAO vdao = new VinhoDAO();
        ArrayList<Vinho> lista = vdao.read();
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new Object[]{
                lista.get(i).getId(),
                lista.get(i).getNome(),
                setClass(lista.get(i).getClassificacao()),});
        }
        rotulos = new ArrayList<byte[]>();
        for (int i = 0; i < lista.size(); i++) {

            rotulos.add(lista.get(i).getRotulo());
        }
    }

    void setImage(int index) {

        if (rotulos.get(index) != null) {
            ImageIcon icone = new ImageIcon(rotulos.get(index));

            icone.setImage(icone.getImage().getScaledInstance(jLabel7.getWidth() - 5, jLabel7.getHeight() - 10, 100));
            jLabel7.setText("");
            jLabel12.setText("");
            jLabel7.setIcon(icone);
            jLabel12.setIcon(icone);
        } else {
            jLabel12.setIcon(null);
            jLabel7.setIcon(null);
            jLabel7.setText("Não possui");
            jLabel12.setText("Não possui");
        }
    }

    String setClass(int value) {
        switch (value) {
            case 0:
                return "Assemblage";

            case 1:
                return "Varietal";
        }
        return "Varietal";
    }

    void setId() {
        VinhoDAO vdao = new VinhoDAO();
        int maior = 1 + vdao.readMaximalId();
        id.setText("" + maior);

    }

