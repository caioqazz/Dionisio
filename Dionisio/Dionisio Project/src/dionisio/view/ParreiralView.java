    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTParreiral.getModel();
        ParreiralDAO pdao = new ParreiralDAO();
        if (jTParreiral.getSelectedRow() >= 0) {
            Object id = jTParreiral.getValueAt(jTParreiral.getSelectedRow(), 0);
            pdao.deleteParreiral(Integer.parseInt(id.toString()));
            readJTable();
        } else {

            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

