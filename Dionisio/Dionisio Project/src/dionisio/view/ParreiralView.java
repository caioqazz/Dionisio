    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTParreiral.getModel();
        modelo.setNumRows(0);
        ParreiralDAO pdao = new ParreiralDAO();
        ArrayList<Parreiral> lista = pdao.read();
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new Object[]{
                lista.get(i).getId(),
                lista.get(i).getQntd(),
                lista.get(i).getArea(),
                lista.get(i).getData(),
                lista.get(i).getNome_prop(),
                lista.get(i).getNome_cepa()
            });
        }
    }

    void setId() {
        ParreiralDAO pdao = new ParreiralDAO();
        int maior = 1 + pdao.readMaximalId();
        id.setText("" + maior);

    }

    public void setComboNomes() {
        ParreiralDAO pdao = new ParreiralDAO();
        ArrayList<String> nomesp = pdao.getNomesPropriedade();
        for (int i = 0; i < nomesp.size(); i++) {
            nome_pro.addItem(nomesp.get(i));

        }
        ArrayList<String> nomesc = pdao.getNomesCepa();
        for (int i = 0; i < nomesc.size(); i++) {
            nome_cepa.addItem(nomesc.get(i));

        }
    }

