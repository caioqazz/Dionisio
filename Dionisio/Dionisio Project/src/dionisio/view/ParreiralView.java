    private void jTParreiralMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTParreiralMouseClicked
        if (jTParreiral.getSelectedRow() >= 0) {

            id1.setText(jTParreiral.getValueAt(jTParreiral.getSelectedRow(), 0).toString());
            qntd1.setValue(jTParreiral.getValueAt(jTParreiral.getSelectedRow(), 1));
            area1.setValue(jTParreiral.getValueAt(jTParreiral.getSelectedRow(), 2));
            data1.setText(jTParreiral.getValueAt(jTParreiral.getSelectedRow(), 3).toString());
            nome_pro1.setText(jTParreiral.getValueAt(jTParreiral.getSelectedRow(), 4).toString());
            nome_cepa1.setText(jTParreiral.getValueAt(jTParreiral.getSelectedRow(), 5).toString());
        }
    }//GEN-LAST:event_jTParreiralMouseClicked

