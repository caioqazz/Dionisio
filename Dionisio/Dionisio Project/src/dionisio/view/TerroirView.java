    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTTerroir.getModel();
        modelo.setNumRows(0);
        TerroirDAO tdao = new TerroirDAO();
        ArrayList<Terroir> lista = tdao.read();
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new Object[]{
                lista.get(i).getRegiao(),
                lista.get(i).getSolo(),
                lista.get(i).getAltitude(),
                lista.get(i).getUmidade(),
                lista.get(i).getIndice_plu(),
                lista.get(i).getTemperatura(),
                lista.get(i).getNome_propriedade()
            });
        }
    }

    public void setComboNomes() {
        TerroirDAO tdao = new TerroirDAO();
        ArrayList<String> nomes = tdao.getNomes();
        for (int i = 0; i < nomes.size(); i++) {
            nome.addItem(nomes.get(i));

        }
    }

