    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTTerroir.getModel();
        TerroirDAO tdao = new TerroirDAO();
        if (jTTerroir.getSelectedRow() >= 0) {
            Object região = jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 0);
            tdao.delete(região.toString());
            readJTable();
        } else {

            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

