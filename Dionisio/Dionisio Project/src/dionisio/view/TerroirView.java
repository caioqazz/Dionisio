    private void jTTerroirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTTerroirMouseClicked
        if (jTTerroir.getSelectedRow() >= 0) {
            regiao1.setText(jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 0).toString());
            solo1.setText(jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 1).toString());
            alt1.setValue(jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 2));
            umi1.setValue(jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 3));
            ind1.setValue(jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 4));
            temp1.setValue(jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 5));
            nome1.setText(jTTerroir.getValueAt(jTTerroir.getSelectedRow(), 6).toString());
        }

    }//GEN-LAST:event_jTTerroirMouseClicked

